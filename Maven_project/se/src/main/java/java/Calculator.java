package java;

public class Calculator {
    public int add(int num1, int num2) {
        int result = num1 + num2;
        return result;
    }
    public int substract(int num1, int num2) {
        int result = num1 - num2;
        return result;
    }

    public int multiply(int num1, int num2) {
        int result = num1 * num2;
        return result;
    }

    public double divide(int num1, int num2) {
        int result = num1/num2;
        return result;
    }
}
